                                /*Bitwise operators*/

#include <stdio.h>

void main(){
    int a,b;
    printf("Enter two numbers>>");
    scanf("%d %d",&a,&b);
    printf("a&b = %d\n",a&b);
    printf("a|b = %d\n",a|b);
    printf("a>>1 = %d\n",a>>1);
    printf("a<<1 = %d\n",a<<1);
    printf("~a = %d\n",~a);
    printf("a^b = %d\n",a^b);
}
